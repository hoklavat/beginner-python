#04- Operations

num1 = 10
num2 = 2.5
print(type(num1))   #Integer
print(type(num2))   #Floating Point
print(2+1)  #Addition
print(2-1)  #Subtraction
print(5/2)  #Division
print(5//2) #Integer Division
print(5*2)  #Multiplication
print(5**2) #Power
print(5%2)  #Remainder
print(5<2)  #Less Than
print(5<=2) #Less Than or Equal
print(5>2)  #Greater Than
print(5>=2) #Greater Than or Equal
print(5==2) #Equal
print(5!=2) #Not Equal
print(int(2.5))
print(float(2))
print(abs(-2))
print(max(1, 2))
print(min(1, 2))
print(pow(2, 5))
